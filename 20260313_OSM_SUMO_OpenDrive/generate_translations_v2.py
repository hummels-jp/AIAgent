def translate_content(content, translations_dict, lang):
    """翻译HTML内容"""
    # 替换所有翻译条目
    for zh, trans in translations_dict.items():
        content = content.replace(zh, trans)
    
    # 替换lang属性
    if lang == 'en':
        content = content.replace('lang="zh-CN"', 'lang="en"')
    elif lang == 'ja':
        content = content.replace('lang="zh-CN"', 'lang="ja"')
    
    return content

